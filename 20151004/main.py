#--------------------------------------------
#自然数のうち，A乗すると下B桁がCになる最小の値を求める
#--------------------------------------------
#A B C は標準入力からスペース区切りで入力される
#--------------------------------------------

if __name__ == '__main__':

    #--パラメータ設定
    A,B,C = map(int,input().split())

    #--ゴリ押しだけどループ
    row = 0
    while True:
        tri = row ** A
        check = str(tri)[-B:].startswith(str(C))
        if(check):
            print(row)
            break
        row = row + 1